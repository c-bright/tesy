class test:
    def __init__(self):
        self.x, self.y = [1, 3]

    def __name(self):
        print("我是一个计数函数")

    def tian_jia(self):
        self.z = pow(self.x, self.y)
        return self.z

    def zong_shu(self):
        print(f"z为{self.z}")

    def run(self):
        self.__name()
        self.tian_jia()
        self.zong_shu()